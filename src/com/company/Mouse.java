package com.company;

public class Mouse {

    private int weight;
    private int distanceFromTom;

    public Mouse(int weight, int distanceFromTom) {
        this.weight = weight;
        this.distanceFromTom = distanceFromTom;
    }

    public int getWeight() {
        return weight;
    }

    public int getDistanceFromTom() {
        return distanceFromTom;
    }
}
